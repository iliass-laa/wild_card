#include"libft.h"

char	*ft_strchr(const char *s, int c)
{
	if(s == NULL)
		return NULL;
	if (*s == '\0' && (char)c == '\0')
		return ((char *)s);
	while (*s)
	{
		if (*s == (char)c)
			return ((char *)s);
		s++;
	}
	if ((char)c == '\0')
		return ((char *)s);
	return (NULL);
}
